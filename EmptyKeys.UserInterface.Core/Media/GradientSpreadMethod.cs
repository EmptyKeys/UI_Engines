
namespace EmptyKeys.UserInterface.Media
{
    /// <summary>
    /// Spread methods for Linear Gradient Brush
    /// </summary>
    public enum GradientSpreadMethod
    {
        /// <summary>
        /// last color fills the area
        /// </summary>
        Pad,

        /// <summary>
        /// gradient is mirrored
        /// </summary>
        Reflect,

        /// <summary>
        /// gradient is repeated
        /// </summary>
        Repeat
    }
}
